        <section id="guidelines">
            <h2>Author Guidelines</h2>

            <p>Authors are invited to submit original and unpublished review/research papers in single-column with a minimum of 12 to 15 pages in length, subjected to similarity less than 15%. Paper submissions are accepted along with a plagiarism report (in Turnitin software only).</p>

            <p>After expert review, the authors may be asked to submit the camera-ready paper. All the authors must follow the guidelines mentioned in: <a href="https://www.springer.com/gp/authors-editors/book-author/book-manuscript-submission">Springer Submission Guidelines</a>.</p>

            <p>Please mention the most relevant one or two sub-theme(s) of the conference which fits your paper.</p>

            <p>Tables, figures, and images should have appropriate captions and be of good quality. Each of these items must be cited inline in the main text of the manuscript.</p>

            <p>Before submitting your paper, please ensure that the English used is clear, concise, and coherent. This is especially important if English is not your first language.</p>

            <p>All submissions to the conference should be free of any form of plagiarism. All submissions with less than 15% similarity index will only be eligible for review, with the quality and scope of the work being taken into consideration. A plagiarism report from Turnitin must also be uploaded while submitting the paper.</p>

            <p>Once the full paper is accepted, author(s) will be requested to submit the camera-ready paper along with a copyright transfer form.</p>

            <p>Any paper which does not follow the guidelines may not be considered for publication in the conference proceedings.</p>

            <p>All the accepted papers must be revised with due comments and suggestions given by reviewers (if any) before the deadlines.</p>

            <p>All approved papers will be processed for publication once the post-conference process has been completed.</p>

            <p>The Publisher maintains the right to exclude any papers from the Work if, in its or its advisers' view, any papers are deemed unsuitable for publication by the Publisher. The publisher retains final decision-making authority.</p>

            <p><strong>Note:</strong><br>
                At least one author should register for the paper to be included in the conference proceedings. Only registered authors will be allowed to present papers and will be issued conference certificates.<br>
                Nobody will be allowed to make more than two presentations during the conference. This is done to ensure diversity and inclusivity of more active participants in the conference.
            </p>
