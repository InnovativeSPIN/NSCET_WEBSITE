            <h2>About Us</h2>
            <p>Our institution offers comprehensive programs across diverse engineering disciplines, including Mechanical Engineering, Electrical and Electronics Engineering (EEE), Electronics and Communication Engineering (ECE), Information Technology (IT), Artificial Intelligence and Data Science (AI & DS), and Computer Science and Engineering (CSE). Each department is dedicated to equipping students with the knowledge and skills essential for excelling in their respective fields. Mechanical Engineering focuses on industries such as power generation, manufacturing, energy, robotics, and aerospace. EEE explores power systems, renewable energy, smart grids, and automation technologies. ECE delves into telecommunications, IoT, VLSI design, and signal processing. IT emphasizes software development, cloud computing, cybersecurity, and data management. AI & DS nurtures expertise in machine learning, deep learning, big data, and predictive analytics, while CSE provides a strong foundation in programming, artificial intelligence, networking, and system architecture. Across all departments, our programs integrate interdisciplinary themes like Health, Environment, Civics, and Entrepreneurship, ensuring students are prepared for the challenges of the modern world. We are committed to inspiring and empowering our students, building their trust and confidence, and fostering innovation and critical thinking. We warmly welcome your interest in our departments and wish you a successful and enriching journey with us.</p>
